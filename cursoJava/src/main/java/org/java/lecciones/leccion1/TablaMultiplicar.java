package org.java.lecciones.leccion1;

import java.util.Arrays;

public class TablaMultiplicar {

    public static void main(String[] args) {
        Integer numero = 8;

        for (int i = 0; i <= 10; i++) {
            Integer resultado = numero * i;
            System.out.println(numero + " * " + i + " = " + resultado);
        }





    }

}
